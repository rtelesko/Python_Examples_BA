# Slicing with Strings

s = "Telesko"

print(s[1:5]) # eles
print(s[1:]) # elesko
print(s[:1]) # T
print(s[:]) # Telesko
print(s[::]) # Telesko
print(s[:-1]) # Telesk
print(s[:-2]) # Teles
print(s[::-1]) # okseleT
print(s[-4:]) # esko
print(s[-4:-2]) # es
print(s[:-4]) # Tel
print(s[::len(s)-1]) # To
print(s[-4:0:-2]) # ee

